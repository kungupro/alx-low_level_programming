#include "3-calc.h"
/**
 * op_add - function that adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum of a + b
 */

int op_add(int a, int b);
{
	return (a + b);
}

/**
 * op_sub - Returns subtraction of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: Results of a - b
 */

int op_sub(int a, int b);
{
	return (a - b);
}

/**
 * op_ mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: Results of a * b
 */

int op_mul(int a, int b);
{
	return (a * b);
}

/**
 * op_div - Returns subtraction of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: Results of a / b
 */

int op_div(int a, int b);
{
	return (a / b);
}
/**
 * op_mod - Returns subtraction of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: Results of a - b
 */

int op_mod(int a, int b);
{
	return (a % b);
}
